    <p class="vraag1tekst">Theory</p>
    <p class="theory">Motivatie is het proces van starten en behouden van doelgericht gedrag.
        Motivatie word vaak omgschreven als de reden waarom mensen iets doen.
        Maar motivatie is niet alleen de redenen om het doelgericht gedrag
         te starten maar ook te behouden. Er zijn 2 soorten motivaties Extrinsieke en Intrinsieke motivatie.
         Extrinsieke motivatie is motivatie die externe voordelen als resultaat hebben zoals geld of sociale erkenning.
         Intrinsieke motivatie is movitavite met interne voordelen dus motivatie die uit je zelf komt,
         dus je doet het niet voor iemand anders maar voor je zelf.
         Maar in het algemeen is motivatie gewoon dopamine, je voelt je goed als je iets hebt gedaan dat je wou doen en je voelt je slecht als je iets niet gedaan hebt wat je wel zou moeten doen.
    </p>

